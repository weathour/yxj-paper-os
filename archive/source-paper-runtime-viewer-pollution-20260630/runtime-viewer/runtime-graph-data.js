(function attachDispatchEdges() {
  const graph = window.PPG_RUNTIME_GRAPH;
  const stageIds = graph.nodes.filter((node) => node.id.startsWith('S')).map((node) => node.id);
  stageIds.forEach((id, index) => {
    graph.edges.push({ id: `d${String(index).padStart(2, '0')}`, kind: 'dispatch', source: 'BUS', target: id, label: 'dispatch' });
  });
})();
